def sync_data_translate(html: str) -> str:
    pattern = re.compile(
        r'(<([a-zA-Z0-9]+)[^>]*\sdata-translate="([^"]+)"[^>]*>)(.*?)(</\2>)',
        re.DOTALL,
    )

    def repl(match):
        open_tag, _tag, key, content, close_tag = match.groups()
        if key not in ru:
            return match.group(0)
        if re.search(r"<[^>]+>", content):
            return match.group(0)
        return f"{open_tag}{ru[key]}{close_tag}"

    return pattern.sub(repl, html)


def process_file(path: Path) -> bool:
    html = path.read_text(encoding="utf-8")
    original = html

    for pattern, repl in REPLACEMENTS:
        html = re.sub(pattern, repl, html)

    html = sync_data_translate(html)

    html = re.sub(
        r'(<input[^>]*data-translate-placeholder="([^"]+)"[^>]*placeholder=")([^"]*)(")',
        lambda m: f"{m.group(1)}{ru[m.group(2)]}{m.group(4)}" if m.group(2) in ru else m.group(0),
        html,
    )

    if html != original:
        path.write_text(html, encoding="utf-8")
        return True
    return False


def main():
    updated = 0
    for path in ROOT.rglob("*.html"):
        if "node_modules" in path.parts or "dist" in path.parts:
            continue
        if "googlebfb7c3e456dd3b31.html" in path.name:
            continue
        if process_file(path):
            updated += 1
            print(f"Updated: {path.relative_to(ROOT)}")

    print(f"\nDone. Updated {updated} files.")


if __name__ == "__main__":
    main()
