function escapeHtml(s) {
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function buildHtml(pageId, content) {
  const lines = [
    "        <!-- seo-article:start -->",
    '        <section class="main-content-section seo-article">',
    '          <div class="main-content-div">',
    '            <div class="main-content">',
    `              <h2 data-translate="seo-${pageId}-h2">${escapeHtml(content.h2)}</h2>`,
  ];
  content.sections.forEach((section, si) => {
    const s = si + 1;
    lines.push(
      `              <h3 data-translate="seo-${pageId}-h3-${s}">${escapeHtml(section.h3)}</h3>`
    );
    section.paragraphs.forEach((p, pi) => {
      lines.push(
        `              <p data-translate="seo-${pageId}-p-${s}-${pi + 1}">${escapeHtml(p)}</p>`
      );
    });
  });
  lines.push(
    "            </div>",
    "          </div>",
    "        </section>",
    "        <!-- seo-article:end -->"
  );
  return lines.join("\n");
}

function collectKeys(pageId, content, langObj) {
  for (const k of Object.keys(langObj)) {
    if (k.startsWith("seo-index-")) delete langObj[k];
  }
  langObj[`seo-${pageId}-h2`] = content.h2;
  content.sections.forEach((section, si) => {
    const s = si + 1;
    langObj[`seo-${pageId}-h3-${s}`] = section.h3;
    section.paragraphs.forEach((p, pi) => {
      langObj[`seo-${pageId}-p-${s}-${pi + 1}`] = p;
    });
  });
}

