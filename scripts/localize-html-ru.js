  const original = html;

  for (const [pattern, replacement] of REPLACEMENTS) {
    html = html.replace(pattern, replacement);
  }

  html = syncDataTranslate(html);

  // Sync placeholder attribute values
  html = html.replace(
    /(<input[^>]*data-translate-placeholder="([^"]+)"[^>]*placeholder=")([^"]*)(")/g,
    (match, before, key, _val, after) => {
      if (!ru[key]) return match;
      return `${before}${ru[key]}${after}`;
    }
  );

