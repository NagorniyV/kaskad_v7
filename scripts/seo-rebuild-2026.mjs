function replaceMeta(html, title, description) {
  html = html.replace(/<title>[\s\S]*?<\/title>/, `<title>${title}</title>`);
  html = html.replace(
    /<meta\s+name="description"\s+content="[^"]*"\s*\/>/,
    `<meta name="description" content="${description}" />`
  );
  html = html.replace(
    /<meta\s+property="og:title"\s+content="[^"]*"\s*\/>/,
    `<meta property="og:title" content="${title}" />`
  );
  html = html.replace(
    /<meta\s+property="og:description"\s+content="[^"]*"\s*\/>/,
    `<meta property="og:description" content="${description}" />`
  );
  html = html.replace(
    /<meta\s+name="twitter:title"\s+content="[^"]*"\s*\/>/,
    `<meta name="twitter:title" content="${title}" />`
  );
  html = html.replace(
    /<meta\s+name="twitter:description"\s+content="[^"]*"\s*\/>/,
    `<meta name="twitter:description" content="${description}" />`
  );
  // schema description if present
  html = html.replace(
    /("description":\s*")([^"]*)(")/g,
    (m, a, _old, c, offset, full) => {
      // only first few in head scripts — safer to skip blanket replace
      return m;
    }
  );
  return html;
}

function replaceH1(html, key, text) {
  const re = new RegExp(
    `(<h1[^>]*data-translate="${key}"[^>]*>)([\\s\\S]*?)(</h1>)`,
    "i"
  );
  if (re.test(html)) return html.replace(re, `$1${text}$3`);
  return html.replace(
    /(<h1 class="hero-title"[^>]*>)([\s\S]*?)(<\/h1>)/i,
    `$1${text}$3`
  );
}

function seoArticleHtml(prefix, keys) {
  // Build from LOCALE keys that start with prefix
  const entries = Object.entries(LOCALE).filter(([k]) => k.startsWith(prefix));
  const order = entries
    .map(([k]) => k)
    .sort((a, b) => {
      const rank = (k) => {
        if (k.endsWith("-h2")) return "0";
        const m = k.match(/h3-(\d+)/);
        if (m) return `1-${m[1].padStart(2, "0")}`;
        const p = k.match(/p-(\d+)-(\d+)/);
        if (p) return `1-${p[1].padStart(2, "0")}-p${p[2]}`;
        if (k.includes("-p-")) return "2";
        return "9";
      };
      return rank(a).localeCompare(rank(b));
    });

  // Better: explicit ordered builder per prefix based on h2, then h3-N with following p
  let h2 = LOCALE[`${prefix}h2`];
  if (!h2) return null;
  let out = `        <section class="main-content-section seo-article">
          <div class="main-content-div">
            <div class="main-content">
              <h2 data-translate="${prefix}h2">${h2}</h2>\n`;

  for (let i = 1; i <= 8; i++) {
    const h3k = `${prefix}h3-${i}`;
    if (!LOCALE[h3k]) continue;
    out += `              <h3 data-translate="${h3k}">${LOCALE[h3k]}</h3>\n`;
    for (let j = 1; j <= 6; j++) {
      const pk = `${prefix}p-${i}-${j}`;
      const pkHtml = `${prefix}p-${i}-${j}-html`;
      if (LOCALE[pkHtml]) {
        out += `              <p data-translate-html="${pkHtml}">${LOCALE[pkHtml]}</p>\n`;
      } else if (LOCALE[pk]) {
        out += `              <p data-translate="${pk}">${LOCALE[pk]}</p>\n`;
      }
    }
  }
  out += `            </div>
          </div>
        </section>`;
  return out;
}

function replaceSeoSection(html, prefix) {
  const block = seoArticleHtml(prefix, LOCALE);
  if (!block) return html;
  const re =
    /<section class="main-content-section seo-article">[\s\S]*?<\/section>/;
  if (re.test(html)) return html.replace(re, block);
  // insert before callback widgets or end of container
  if (html.includes('<div id="site-callback-widgets"></div>')) {
    return html.replace(
      '<div id="site-callback-widgets"></div>',
      `${block}\n          <div id="site-callback-widgets"></div>`
    );
  }
  return html;
}

